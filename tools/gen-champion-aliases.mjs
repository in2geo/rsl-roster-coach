// ── tools/gen-champion-aliases.mjs ───────────────────────────────────────────
// Generates seeds/<N>_champion_aliases_data.sql from the current (deduped) champions
// table. Two alias sources:
//   (1) truncation — the mis-seed bare names removed in seed 27, mapped to their
//       canonical champion so text that still uses the short form resolves.
//   (2) shortform — community forms (drop the "X the Y" epithet, or a leading adjective
//       → last word) that resolve to exactly ONE champion and aren't themselves a full
//       champion name. Ambiguous forms are skipped.
// The emitted seed is committed and reconstructs the data; this tool is just the writer.
//   node --env-file=.env.local tools/gen-champion-aliases.mjs

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import pg from 'pg';

const norm = (s) => String(s ?? '').trim().toLowerCase();
const sqlStr = (s) => `'${String(s).replace(/'/g, "''")}'`;

// Bare mis-seed names removed in seed 27 → canonical champion.
const TRUNCATIONS = {
  'Corvis': 'Corvis the Corruptor', 'Criodan': 'Criodan the Blue',
  'Drexthar': 'Drexthar Bloodtwin',
  'Fahrakin': 'Fahrakin the Fat', 'Fodbor': 'Fodbor the Bard',
  'Folan': 'Folan Silverhart', 'Gnishak': 'Gnishak Verminlord',
  'Lanakis': 'Lanakis the Chosen', 'Marius': 'Marius the Gallant',
  'Mavara': 'Mavara the Web Diviner', 'Morag': 'Morag Bronzelock',
  'Pelops': 'Pelops the Victor', 'Quargan': 'Quargan the Crowned',
  'Richtoff': 'Richtoff the Bold', 'Sabrael': 'Sabrael the Distant',
  'Scyl': 'Scyl of the Drakes', 'Staltus': 'Staltus Dragonbane',
  'Teodor': 'Teodor the Savant', 'Tholin': 'Tholin Foulbeard',
};

// Community short forms of a full name: pre-"the" part, and the last word (drops a
// leading adjective, e.g. Glorious Pallas → Pallas). First word is intentionally NOT
// aliased (leading adjectives like Dark/Steel are shared noise).
function shortForms(name) {
  const forms = new Set();
  const m = name.match(/^(.+?)\s+the\s+/i);
  if (m) forms.add(m[1].trim());
  const words = name.trim().split(/\s+/);
  if (words.length > 1) forms.add(words[words.length - 1]);
  return [...forms].filter((f) => f && norm(f) !== norm(name) && f.length >= 3);
}

const client = new pg.Client({ connectionString: process.env.SUPABASE_DB_URL, ssl: { rejectUnauthorized: false } });
await client.connect();
const { rows: champs } = await client.query(
  "select name from champions where game_id = 'raid_shadow_legends'");
await client.end();

const fullByNorm = new Map(champs.map((c) => [norm(c.name), c.name]));

// alias(norm) → { alias, canonical, source }  — first writer wins; ambiguity drops it.
const chosen = new Map();
const ambiguous = new Set();
function offer(alias, canonical, source) {
  const k = norm(alias);
  if (ambiguous.has(k)) return;
  // Skip an alias that is itself a full champion's name (not this one).
  if (fullByNorm.has(k) && norm(fullByNorm.get(k)) !== norm(canonical)) { ambiguous.add(k); chosen.delete(k); return; }
  if (chosen.has(k) && norm(chosen.get(k).canonical) !== norm(canonical)) { ambiguous.add(k); chosen.delete(k); return; }
  if (!chosen.has(k)) chosen.set(k, { alias, canonical, source });
}

for (const [bare, canonical] of Object.entries(TRUNCATIONS)) offer(bare, canonical, 'truncation');
for (const c of champs) for (const sf of shortForms(c.name)) offer(sf, c.name, 'shortform');

const rows = [...chosen.values()].sort((a, b) => a.canonical.localeCompare(b.canonical) || a.alias.localeCompare(b.alias));

const lines = [
  '-- 28 — champion_aliases data (generated by tools/gen-champion-aliases.mjs).',
  '-- alias → canonical champion, for resolving name-keyed TEXT sources. Idempotent',
  '-- (ON CONFLICT DO NOTHING on the unique lower(alias) index). Regenerate after adding',
  `-- champions. Sources: truncation (seed-27 removals) + shortform. ${rows.length} aliases.`,
  '',
];
for (const r of rows) {
  lines.push(
    `insert into champion_aliases (champion_id, alias, source)\n` +
    `select id, ${sqlStr(r.alias)}, ${sqlStr(r.source)} from champions\n` +
    `where game_id='raid_shadow_legends' and name=${sqlStr(r.canonical)} on conflict do nothing;`);
}
const outPath = path.join(path.dirname(fileURLToPath(import.meta.url)), '..', 'seeds', '28_champion_aliases_data.sql');
fs.writeFileSync(outPath, lines.join('\n') + '\n');
console.log(`Wrote ${rows.length} aliases (${[...chosen.values()].filter(r=>r.source==='truncation').length} truncation, ${[...chosen.values()].filter(r=>r.source==='shortform').length} shortform), ${ambiguous.size} ambiguous skipped`);
console.log(`→ ${outPath}`);
console.log('sample:', rows.slice(0, 8).map(r => `${r.alias}→${r.canonical}`).join(', '));
