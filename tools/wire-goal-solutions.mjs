/**
 * wire-goal-solutions.mjs — remediate SKELETON goal_solutions (proposed rows with no tags, which
 * computeTagCoverage skips → the goal is permanently "unmet"). Maps each skeleton solution's LABEL
 * to the tag(s) it names and GENERATES a committed seed that wires goal_solution_tags + approves
 * the solution. Does NOT apply anything — writes seeds/2026-07-12_wire_goal_solution_tags.sql for
 * human review, then apply via tools/apply-seed-pooler.mjs (per the CLAUDE.md hard rule).
 * Labels it can't map cleanly (stat thresholds, negative constraints, OR-labels) are REPORTED, not
 * wired. Read-only against the DB.
 *
 * Usage: node --env-file=.env.local tools/wire-goal-solutions.mjs
 */
import fs from 'fs';
import { createClient } from '@supabase/supabase-js';

const s = createClient(process.env.SUPABASE_URL.replace(/\/rest\/v1\/?$/, ''), process.env.SUPABASE_SERVICE_KEY, { global: { fetch } });

// Curated label → tag(s). Only mechanically-clear mappings; the label names the tag(s).
const LABEL_MAP = {
  'Block Revive': ['Block Revive'],
  'AoE Stun': ['AoE Stun'],
  'AoE Freeze': ['AoE Freeze'],
  'AoE Decrease Turn Meter': ['AoE Decrease Turn Meter'],
  'Multi-Hit A1 champions + Counterattack buff': ['Multi-Hit A1', 'Counterattack'],
  'Decrease Turn Meter + Decrease SPD': ['Decrease Turn Meter', 'Decrease Speed'],
  'Decrease Turn Meter only': ['Decrease Turn Meter'],
  'Decrease Turn Meter': ['Decrease Turn Meter'],
  'Poison stacking': ['Poison'],
  'Block Cooldowns': ['Block Cooldowns'],
  'Multi-Hit A1 champions (5 × 2+ hits = 10+)': ['Multi-Hit A1'],
  'Multi-Hit A1 champions (5 x 2+ hits = 10+)': ['Multi-Hit A1'],
  'Multi-Hit A1 champions + Ally Attack': ['Multi-Hit A1', 'Ally Attack'],
  'Increase SPD buff (team-wide)': ['Increase Speed'],
  'Cleanse (removes SPD debuff)': ['Cleanse'],
  'Cleanse': ['Cleanse'],
  'AoE Damage + Decrease DEF': ['AoE Damage', 'Decrease Defense'],
  'HP Burn': ['HP Burn'],
  'Block Revive + burst damage': ['Block Revive'],
  'Block Revive + careful minion management': ['Block Revive'],
};
// Deliberately NOT wired (need a different mechanism, flagged in the report):
//   'AoE Stun or Freeze'                       — OR: the goal already has separate AoE Stun / AoE Freeze solutions
//   'Kill revived minions immediately'         — vague; no single tag
//   'High base ACC (150+ above threshold)'     — a stat threshold, belongs in stat_threshold_checks, not tag coverage
//   'Avoid Counterattack/Reflect Damage entirely' — a NEGATIVE constraint (don't-bring), not a coverage tag

const { data: tags } = await s.from('tags').select('name');
const vocab = new Set((tags ?? []).map(t => t.name));

// Every skeleton solution (no tags) on a goal with no approved+tagged solution.
const { data: goals } = await s.from('goals')
  .select('id, description, goal_solutions(id, label, status, goal_solution_tags(id))');
const skeletons = []; // {solId, label, goalId, goalDesc}
const goalSolvable = new Map(); // goalId → will it become satisfiable after wiring?
for (const g of goals ?? []) {
  const ok = (g.goal_solutions ?? []).some(sol => sol.status === 'approved' && (sol.goal_solution_tags ?? []).length > 0);
  if (ok) continue;
  goalSolvable.set(g.id, goalSolvable.get(g.id) ?? false);
  for (const sol of g.goal_solutions ?? []) {
    if ((sol.goal_solution_tags ?? []).length > 0) continue;
    skeletons.push({ solId: sol.id, label: sol.label ?? '', goalId: g.id, goalDesc: g.description });
  }
}

const wired = [];        // {solId, label, tags, goalId}
const flagged = new Map(); // label → count (unmapped)
const missingTag = new Set();
for (const sk of skeletons) {
  const mapped = LABEL_MAP[sk.label];
  if (!mapped) { flagged.set(sk.label, (flagged.get(sk.label) ?? 0) + 1); continue; }
  const bad = mapped.filter(t => !vocab.has(t));
  if (bad.length) { bad.forEach(t => missingTag.add(t)); flagged.set(sk.label, (flagged.get(sk.label) ?? 0) + 1); continue; }
  wired.push({ ...sk, tags: mapped });
  goalSolvable.set(sk.goalId, true);
}

// ── Generate the committed seed ──────────────────────────────────────────────
const esc = (x) => String(x).replace(/'/g, "''");
let sql = `-- Wire SKELETON goal_solutions → tags + approve them, so computeTagCoverage can satisfy the goal.
-- Generated by tools/wire-goal-solutions.mjs from mechanically-clear label→tag mappings; REVIEW
-- before applying. Idempotent. ${wired.length} solution(s) across ${[...goalSolvable.values()].filter(Boolean).length} goal(s). 2026-07-12.
`;
for (const w of wired) {
  const tagList = w.tags.map(t => `'${esc(t)}'`).join(', ');
  sql += `\n-- ${esc(w.goalDesc.slice(0, 70))} | "${esc(w.label)}"\n`;
  sql += `insert into goal_solution_tags (goal_solution_id, tag_id)\n`;
  sql += `select '${w.solId}', id from tags where name in (${tagList})\n`;
  sql += `  and id not in (select tag_id from goal_solution_tags where goal_solution_id = '${w.solId}');\n`;
  sql += `update goal_solutions set status = 'approved' where id = '${w.solId}';\n`;
}
const outPath = 'seeds/2026-07-12_wire_goal_solution_tags.sql';
fs.writeFileSync(outPath, sql);

// ── Report ───────────────────────────────────────────────────────────────────
const goalsFixed = [...goalSolvable.values()].filter(Boolean).length;
console.log(`\nSkeleton solutions on permanently-unmet goals: ${skeletons.length}`);
console.log(`  WIRED (clear label→tag): ${wired.length} solution(s) → un-breaks ${goalsFixed} goal(s)`);
console.log(`  → seed written: ${outPath} (review, then: node tools/apply-seed-pooler.mjs ${outPath})\n`);
if (missingTag.size) console.log(`  ⚠ mapped tags NOT in vocab (fix LABEL_MAP): ${[...missingTag].join(', ')}\n`);
console.log(`FLAGGED — not wired, need a different mechanism (${[...flagged.values()].reduce((a, c) => a + c, 0)} solution(s)):`);
for (const [lab, cnt] of [...flagged.entries()].sort((a, b) => b[1] - a[1]))
  console.log(`  ${cnt.toString().padStart(3)}×  ${lab}`);
console.log('\nFlagged labels are stat-threshold goals (ACC), negative constraints (avoid Counterattack), or OR-labels');
console.log('already covered by sibling solutions — they should NOT be tag-coverage rows. Handle separately.\n');
